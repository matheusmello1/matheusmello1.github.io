function esconder1() {
  var enlace_1 = document.getElementById('conteudo_1');
  var troca1 = document.getElementById('enlace_1');
  enlace_1.style.display = enlace_1.style.display === 'none' ? '' : 'none';
   if (troca1.innerHTML=="Mostrar Conteúdo"){
  	troca1.innerHTML="Ocultar Conteúdo"
  } else {
  	troca1.innerHTML="Mostrar Conteúdo"
  }
}

